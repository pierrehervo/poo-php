<?php

class Contact
{
    public $nom;
    public $prenom;
    public $numero;
    public $email;

    public function __construct($name, $prenom, $numero, $email)
    {
     $this->name = $nom;   
     $this->prenom = $prenom;   
     $this->numero = $numero;   
     $this->email = $email;   
    }
}